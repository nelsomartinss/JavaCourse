package Lessons.OtherTopics;

public class BitwiseOperators {
    void main(){
        int n1, n2;

        n1 = 89;
        n2 = 60;

        // Operators more commonly used in low-level programming
        System.out.println(n1 & n2); // 24
        System.out.println(n1 | n2); // 125
        System.out.println(n1 ^ n2); // 101
    }
}
